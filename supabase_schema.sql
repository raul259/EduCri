-- ---------------------------------------------------------------------------
-- Helper role check from JWT metadata
-- ---------------------------------------------------------------------------
create or replace function public.is_moderator()
returns boolean
language sql
stable
as $$
    select coalesce((auth.jwt() -> 'user_metadata' ->> 'role') = 'moderador', false)
        or coalesce((auth.jwt() -> 'app_metadata' ->> 'role') = 'moderador', false);
$$;

-- ---------------------------------------------------------------------------
-- Classes
-- ---------------------------------------------------------------------------
create table if not exists public.classes (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    name text not null,
    category text not null default 'otros',
    teacher text not null,
    classroom text not null,
    day text not null,
    time text not null,
    description text null,
    pdf_name text null default 'temario.pdf',
    color text null default 'from-blue-500 to-cyan-400',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    updated_by uuid null references auth.users(id) on delete set null
);
alter table public.classes
    add column if not exists updated_at timestamptz not null default now(),
    add column if not exists updated_by uuid null references auth.users(id) on delete set null,
    add column if not exists pdf_url text null;

create index if not exists classes_user_id_idx on public.classes(user_id);
alter table public.classes enable row level security;
alter table public.classes
    drop constraint if exists classes_day_valid,
    drop constraint if exists classes_time_valid;
alter table public.classes
    add constraint classes_day_valid check (day in ('Lunes','Martes','Miércoles','Jueves','Viernes','Sábado','Domingo')),
    add constraint classes_time_valid check (time ~ '^([01][0-9]|2[0-3]):[0-5][0-9]$');


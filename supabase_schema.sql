-- ---------------------------------------------------------------------------
-- Trigger to auto-create teacher profile on signup
-- ---------------------------------------------------------------------------
create or replace function public.handle_new_user_teacher_profile()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    insert into public.teacher_profiles (
        user_id,
        full_name,
        birth_date,
        phone,
        has_cds,
        received_holy_spirit,
        teaching_experience,
        approval_status
    )
    values (
        new.id,
        coalesce(new.raw_user_meta_data ->> 'full_name', ''),
        nullif(new.raw_user_meta_data ->> 'birth_date', '')::date,
        nullif(new.raw_user_meta_data ->> 'phone', ''),
        coalesce((new.raw_user_meta_data ->> 'has_cds')::boolean, false),
        coalesce((new.raw_user_meta_data ->> 'received_holy_spirit')::boolean, false),
        coalesce(new.raw_user_meta_data ->> 'teaching_experience', ''),
        case
            when coalesce(new.raw_user_meta_data ->> 'role', 'profesor') = 'moderador' then 'approved'
            else 'pending'
        end
    )
    on conflict (user_id) do update
    set
        full_name = excluded.full_name,
        birth_date = excluded.birth_date,
        phone = excluded.phone,
        has_cds = excluded.has_cds,
        received_holy_spirit = excluded.received_holy_spirit,
        teaching_experience = excluded.teaching_experience,
        updated_at = now();

    return new;
end;
$$;

drop trigger if exists on_auth_user_created_teacher_profile on auth.users;
create trigger on_auth_user_created_teacher_profile
after insert on auth.users
for each row execute function public.handle_new_user_teacher_profile();


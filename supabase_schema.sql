-- Optional helper view for moderator dashboard
create or replace view public.attendance_summary_by_teacher as
select
    ar.teacher_id,
    coalesce(tp.full_name, u.email) as teacher_name,
    count(*) filter (where ar.status = 'present') as present_count,
    count(*) filter (where ar.status = 'absent') as absent_count,
    count(*) filter (where ar.status = 'late') as late_count
from public.attendance_records ar
left join public.teacher_profiles tp on tp.user_id = ar.teacher_id
left join auth.users u on u.id = ar.teacher_id
group by ar.teacher_id, teacher_name;
